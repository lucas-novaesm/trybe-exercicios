// Faça cinco programas, um para cada operação aritmética básica. 
// Seu programa deve ter duas constantes, a e b, definidas no começo com os valores que serão operados. Faça programas para:

// Adição (a + b)
// Subtração (a - b)
// Multiplicação (a * b)
// Divisão (a / b)
// Módulo (a % b)

function soma (a, b) {
    return a + b;
};

function subtracao (a, b) {
    return a - b;
};

function mult (a, b) {
    return a * b;
};

function divi (a, b) {
    return a / b;
};

function modu (a, b) {
    return a % b;
}